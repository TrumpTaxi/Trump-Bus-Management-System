    private void normalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_normalActionPerformed
        String scheID = tf_scheID.getText();
        
        try{
            SeatTier st = new SeatTier();
            Seat s = new Seat();
            s.setScheID(scheID);
            s = st.seatprice(s);
                if (s == null){
                    JOptionPane.showMessageDialog(this, "Cannot found seat details.");
                } else {
                    DefaultComboBoxModel model2 = (DefaultComboBoxModel)seatno.getModel();
                    model2.removeAllElements();
                    int count = s.getNormal();
                    for(int a=1;a<=count;a++)
                        model2.addElement(a);
                }
                
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        
        
    }//GEN-LAST:event_normalActionPerformed

