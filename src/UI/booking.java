    private void seatnoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_seatnoActionPerformed
        String scheID = tf_scheID.getText();
        
        try{
            SeatTier st = new SeatTier();
            Seat s = new Seat();
            s.setScheID(scheID);
            s = st.seatprice(s);
                if (s == null){
                    JOptionPane.showMessageDialog(this, "Cannot found seat details.");
                } else {
                    int num = (int)seatno.getSelectedItem();
                    double totalprice;
                    if(normal.isSelected())
                        totalprice = s.getPriceN()*num;
                    else
                        totalprice = s.getPriceVip()*num;
                    totalp.setText(totalprice+"");
                }
                
            } catch (Exception ex) {
            }

    }//GEN-LAST:event_seatnoActionPerformed

